#import "NSDate+Format.h"

@implementation NSDate (Format)

@dynamic year;
@dynamic month;
@dynamic day;
@dynamic hour;
@dynamic minute;
@dynamic second;
@dynamic weekday;

- (NSInteger)year {
    return [[NSCalendar currentCalendar] components:NSCalendarUnitYear fromDate:self].year;
}
- (NSInteger)month {
    return [[NSCalendar currentCalendar] components:NSCalendarUnitMonth fromDate:self].month;
}
- (NSInteger)day {
    return [[NSCalendar currentCalendar] components:NSCalendarUnitDay fromDate:self].day;
}
- (NSInteger)hour {
    return [[NSCalendar currentCalendar] components:NSCalendarUnitHour fromDate:self].hour;
}
- (NSInteger)minute {
    return [[NSCalendar currentCalendar] components:NSCalendarUnitMinute fromDate:self].minute;
}
- (NSInteger)second {
    return [[NSCalendar currentCalendar] components:NSCalendarUnitSecond fromDate:self].second;
}
- (NSInteger)weekday {
    //这里会差一天 我也是醉了
    NSInteger week = [[NSCalendar currentCalendar] components:NSCalendarUnitWeekday fromDate:self].weekday;
    week = week - 1;
    return week ?: 7;
}
- (NSString*)weekdayStr {
    NSString *weekday = nil;
    switch (self.weekday) {
        case 1:weekday = @"一";break;
        case 2:weekday = @"二";break;
        case 3:weekday = @"三";break;
        case 4:weekday = @"四";break;
        case 5:weekday = @"五";break;
        case 6:weekday = @"六";break;
        default:weekday = @"日"; break;
    }
    return weekday;
}
//算出今天凌晨时间
- (NSDate*)firstTime {
    int64_t currSecond = self.timeIntervalSince1970;
    currSecond = currSecond - self.hour * 60 * 60 - self.minute * 60 - self.second;
    NSDate * currDate = [NSDate dateWithTimeIntervalSince1970:currSecond];
    return currDate;
}
//算出明天凌晨时间
- (NSDate*)lastTime {
    int64_t currSecond = self.timeIntervalSince1970;
    currSecond = currSecond - self.hour * 60 * 60 - self.minute * 60 - self.second;
    currSecond = currSecond + (24 * 60 * 60);
    currSecond = currSecond - 1;
    NSDate * currDate = [NSDate dateWithTimeIntervalSince1970:currSecond];
    return currDate;
}
+ (NSDate*)dateWithFormat:(NSString *)format
{
    NSDateFormatter * df2 = [[NSDateFormatter alloc] init];
    [df2 setDateFormat:@"yyyy-MM-dd HH:mm:ss"];
    NSDate * date =[df2 dateFromString:format];
    return date;
}
